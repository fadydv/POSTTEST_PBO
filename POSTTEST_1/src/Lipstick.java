class Lipstick {
    int id;
    String namaProduk;
    String brand;
    String warna;
    String undertone;
    int harga;

    Lipstick(int id, String namaProduk, String brand, String warna, String undertone, int harga) {
        this.id = id;
        this.namaProduk = namaProduk;
        this.brand = brand;
        this.warna = warna;
        this.undertone = undertone;
        this.harga = harga;
    }
}